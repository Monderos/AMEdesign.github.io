<p class="paragraph">Printul realizat pe o imprimantă inkjet este un print realizat prin aplicarea unor picături foarte fine pe un anumit material. Tehnologiile inkjet sunt diferențiate în funcție de tipul cernelii (bazate pe apă, solvent, cu pigmenți, etc), prin modalitatea de aplicare a picăturilor (termice, piezo, etc) și prin rezoluția lor (în funcție de asta și de tipul de cerneală folosit, ele sunt folosite pentru aplicații exterioare sau interioare).

Această tehnologie este în toate componentele ei digitală (fișier digital precum și controlul mărimii și aruncării picăturilor pe suport) și din acest motiv se uzitează folosirea expresiei print digital atunci în special când se folosește tehnologia inkjet.

Printul realizat în tehnologie fotografică este un proces hibrid. </p></br><p class="paragraph">Expunerea materialului fotosensibil (sensibil la lumină) se controlează digital, modulându-se lumina provenită de la trei laseri RGB cu informația citită dintr-un fișier grafic digital tif, jpg, etc. Developarea este analogică, printr-un proces chimic în care substanțele chimice reacționează cu granulele de halogenură de argint din straturile fotosensibile ale materialului expus, în funcție de gradul lor de expunere la cele trei componente RGB. Procesul chimic propriu-zis conferă un aspect calitativ distinct, cu specificități evidente.

Comparația se poate face după mai multe criterii:
</p>
</br>

<p class="b paragraph text-uppercase">1. CALITATEA</p>
</br>
<p class=" paragraph">
Progresul realizat în domeniul imprimării inkjet a condus la performanțe din ce în ce mai bune însă calitatea unui print în tehnologie fotografică nu poate fi încă egalat din simplul fapt că această tehnologie nu folosește puncte de raster (ton continuu), deci un pixel din imaginea grafică nu mai este divizat în puncte de tipar. Aspectul calitativ este similar cu un print offset la 4000 dpi. La tehnologia inkjet, această divizare în puncte de tipar se poate ușor observa la tipărirea unor tonalități foarte deschise.
</p>
</br>
<p class="b paragraph text-uppercase">2. diversitatea de materiale pe care se printează</p>
</br>
<p class=" paragraph">
Din acest punct de vedere, tehnologia inkjet deține supremația, pentru că sunt extrem de multe suporturi de imprimare ce pot fi folosite, de multe ori însă optimizate pentru o anumită tehnologie inkjet. Deși în tehnologia fotografică sunt mai puție materiale: hârtia super mată, mată, lucioasă, metalică, canvas și filmul de casetă luminoasă (ar mai fi câteva tipuri de materiale fotosensibile dar datorită utilizării reduse, nu le mai folosim) numărul de aplicații nu este deloc redus.
</p>
</br>
<p class="b paragraph text-uppercase">3. durabilitatea printului.</p>
</br>
<p class=" paragraph">
Materialele fotosensibile de la Kodak și Fuji au ca durată de viață 200 de ani în condiții de arhivare, durata scăzând în funcție de condițiile de păstrare. Pentru condițiile tipice de iluminare dintr-un muzeu, durata ajunge la 100 de ani, scăzând pentru casetele luminoase la 5-10 ani, în funcție de durata de folosire a casetei. La tehnologia inkjet se folosesc materiale diverse, cu durata de viață mai scăzută pentru suporturile mai ieftine și de durată comparabilă cu a materialelor fotosensibile pentru materialele de calitate superioară. Trebuie amintit că în cazul tehnologiei inkjet, durata de viață este cea a combinației suport + cerneală, nu doar a uneia dintre componente.
</p>
</br>
<p class="b paragraph text-uppercase">4. prețul consumabilelor.</p>
</br>
<p class=" paragraph">
Imprimantele cu tehnologie inkjet au mai multe moduri de printare iar comparația trebuie făcută doar la cel mai bun mod de printare și nu în cel economic dar și al duratei de existență a printului (cerneală + suport). În cel mai bun mod de printare, consumul de cerneală este simțitor mai mare decât în modul economic, cu influență directă asupra prețului final, mai ales că nu trebuie socotit doar costul cernelii de pe hârtie ci și costul necesar curățării capetelor. De asemenea, materialele de calitate, cu durată mare de viață, sunt și ele scumpe, mai scumpe decât cele fotosensibile. Totuși, la prețul final contribuie și prețul echipamentului care în cazul printului fotografic este mult mai mare.

Una peste alta, costul final de printare în cele două tehnologii este comparabil (subliniez, în modul cel mai bun de printare inkjet), astfel încât trebuie subliniat faptul că întotdeauna o calitate înaltă de print se va revedea în costul final al lui.
</p>
</br>
