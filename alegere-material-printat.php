<p class="b paragraph text-uppercase">Hârtia mată</p>
</br>
<p class="paragraph">
Fotografii, în general, sunt persoane cumpătate. Activi, energici, dar calculați în ceea ce privește afișarea propriilor fotografii. Nu doresc reflexe ale luminii necontrolate pe imagine, doar ei sunt stăpânii luminii, vrăjitorii care au creat acele imagini. Hârtia mată este foarte apreciată de către fotografi (95% dintre fotografi aleg hârtia mată pentru print) pentru că le pune mai puține probleme de expunere a tablourilor în lumina existentă și nu întotdeauna foarte potrivită din expoziții, muzee, holurile de așteptare din diverse instituții sau chiar din sufrageria sau dormitorul de acasă. Printul pe hartie mata nu face niciun compromis in redarea corespunzatoare a imaginii, ofera un aspect viu al culorilor, a densităților lor si a contrastului necesar.
</p>
</br>
<p class="b paragraph text-uppercase">Hârtia lucioasă </p>
</br>
<p class="paragraph">
În publicitate, mai exact în aplicațiile care  rolul de a atrage privirea tuturor, suportul lucios este cel mai potrivit. Mai ales dacă există în imagine culori vii, cu aspect sticlos, contraste puternice, definiție bună a contururilor și a detaliilor, un print de calitate va atrage imediat privirea tuturor. Hârtia lucioasă printată în tehnologie fotografică este tot ce-i trebuie unui brand, pentru a fi puternic susținut vizual.
</p>
