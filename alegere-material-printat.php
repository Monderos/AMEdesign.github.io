<p class="b paragraph text-uppercase">Hârtia metalică</p>
</br>
<p class="paragraph">
Hârtia metalică fotosensibilă se prezintă singură. Nu doar prin numele ei ci și prin aplicațiile care o cer de la sine. Fie că este vorba de printarea unor imagini care conțin obiecte metalice (ceasuri, bijuterii, mașini, etc) fie că sunt chiar peisaje suprarealiste sau asumate ca fiind purtătoare a unor culori ieșite din comun, hârtia metalică este perfectă. Obiectele din imagine par a fi chiar din aur, din argint sau pur și simplu sunt metalizate într-un mod desăvârșit.
</p>
</br>

<p class="b paragraph text-uppercase">Hârtia canvas </p>
</br>
<p class="paragraph">
Multă vreme pictura a dominat vizualul prin infinitele posibilități de exprimare prin culori, tușe specifice și subiecte abordate. Apariția fotografiei a determinat acceptarea unanimă a coexistenței celor două tehnici de imortalizare a unor imagini, peisaje, portrete, imagini abstracte sau de atmosferă. Pictura a fost de multe ori privită ca sursă de inspirație în fotografie, deseori imitând nu doar aspectul pictural al unor imagini ba chiar și suportul folosit de către pictori, pânza, așa numitul material canvas. Există și în tehnologia fotografică de printare un material fotosensibil numit canvas. Materialul are un aspect rugos, cu o textură ce imită pânza de pictură. O imagine potrivită, fie că e vorba de un peisaj mirific, pictural sau de o reproducere a unei picturi, este extrem de bine pusă în valoare pe acest material.</p>
