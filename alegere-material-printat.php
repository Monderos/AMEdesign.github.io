<p class="paragraph">
Sunt persoane care iubesc imaginea pură, nealterată nici de cel mai mai mic reflex al luminii în mediul în care sunt privite. Indiferent de tipul și intensitatea iluminării, pe printul pe această hârtie super mată nu se va vedea nicio reflexie. Trebuie știut că pe hârtia super mată culorile obținute nu vor fi foarte saturate și nici contrastele nu vor putea fi puternice. Acest tip de hârtie este foarte bine pusă în valoare de imagini vechi, sepia sau alb negru, dar și de imagini color care se doresc ușor estompate.
